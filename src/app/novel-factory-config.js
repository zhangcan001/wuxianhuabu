export function normalizeNovelBodyTemplate(template, providerPreset = null) {
  const fallback = providerPreset?.bodyTemplate || NOVEL_API_BODY_TEMPLATE_DEFAULT;
  const raw = String(template || "").trim();
  if (!raw) return fallback;
  const looksLikeOldFactoryTemplate =
    raw.includes("转换模板") ||
    raw.includes("小说内容") ||
    raw.includes("把小说转成可生产的漫剧资产") ||
    raw.includes("人物3视图及面部特写提示词");
  const hasStepInput = raw.includes("{{input}}") && raw.includes("当前步骤输入");
  const hasRequiredSlots = raw.includes("{{template}}") && raw.includes("{{schema}}") && raw.includes("{{model}}");
  if (looksLikeOldFactoryTemplate || !hasStepInput || !hasRequiredSlots) return fallback;
  return raw;
}

