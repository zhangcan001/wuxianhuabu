const COMMERCIAL_REQUIRED_MARKERS = [
  "commercialReadiness",
  "visualLock",
  "continuity",
  "opening frame",
  "closing frame",
  "riskControl",
  "交付",
];

export function assessCommercialTemplateCoverage(templates = []) {
  const content = (Array.isArray(templates) ? templates : [])
    .map((item) => typeof item === "string" ? item : item?.content || "")
    .join("\n");
  const missing = COMMERCIAL_REQUIRED_MARKERS.filter((marker) => !content.includes(marker));
  return {
    ready: missing.length === 0,
    missing,
    checked: COMMERCIAL_REQUIRED_MARKERS.length,
  };
}
