export function buildClearedProjectState(deps) {
  const {
    defaultTimelineState,
    defaultPromptFactoryState,
    defaultTemplateCenterState,
    defaultStylePresetCenterState,
    defaultModelParamCenterState,
    defaultExportPresetCenterState,
    defaultCollaborationState,
    defaultArchiveState,
    defaultPerformanceSettings,
    defaultEpisodes,
  } = deps;
  return {
    nextNodeId: 1,
    nodes: [],
    edges: [],
    resources: [],
    timeline: defaultTimelineState(),
    promptFactory: defaultPromptFactoryState(),
    templateCenter: defaultTemplateCenterState(),
    stylePresetCenter: defaultStylePresetCenterState(),
    modelParamCenter: defaultModelParamCenterState(),
    exportPresetCenter: defaultExportPresetCenterState(),
    collaborationState: defaultCollaborationState(),
    archiveState: defaultArchiveState(),
    exportHistory: [],
    performanceSettings: defaultPerformanceSettings(),
    generationQueue: [],
    productionEvents: [],
    queueRunning: false,
    activeRenderRequestId: "",
    episodes: defaultEpisodes(),
    activeEpisodeId: "episode-1",
    businessProject: null,
    view: { x: 0, y: 0, scale: 1 },
    menu: null,
    nodeMenu: null,
    edgeMenu: null,
    currentProjectPath: "",
    autoSaveState: "",
  };
}

export function buildAppliedProjectState(project, path, deps) {
  const {
    inferNextNodeId,
    defaultTimelineState,
    defaultPromptFactoryState,
    defaultTemplateCenterState,
    defaultStylePresetCenterState,
    defaultModelParamCenterState,
    defaultExportPresetCenterState,
    defaultCollaborationState,
    defaultArchiveState,
    defaultPerformanceSettings,
  } = deps;
  return {
    nextNodeId: inferNextNodeId(project.nodes),
    nodes: project.nodes,
    edges: project.edges,
    view: project.view,
    settings: project.settings,
    resources: project.resources || [],
    timeline: project.timeline || defaultTimelineState(),
    promptFactory: project.promptFactory || defaultPromptFactoryState(),
    templateCenter: project.templateCenter || defaultTemplateCenterState(),
    stylePresetCenter: project.stylePresetCenter || defaultStylePresetCenterState(),
    modelParamCenter: project.modelParamCenter || defaultModelParamCenterState(),
    exportPresetCenter: project.exportPresetCenter || defaultExportPresetCenterState(),
    collaborationState: project.collaborationState || defaultCollaborationState(),
    archiveState: project.archiveState || defaultArchiveState(),
    exportHistory: project.exportHistory || [],
    performanceSettings: project.performanceSettings || defaultPerformanceSettings(),
    generationQueue: project.generationQueue || [],
    productionEvents: project.productionEvents || [],
    queueRunning: false,
    activeRenderRequestId: "",
    episodes: project.episodes,
    activeEpisodeId: project.activeEpisodeId,
    businessProject: project.businessProject || null,
    currentProjectPath: path || "",
  };
}
